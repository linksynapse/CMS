package com.bluzen.acs.obj;

public class PsInfo{
	int Value;
	String Name;
	int Count;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public int getCount() {
		return Count;
	}
	public void setCount(int count) {
		Count = count;
	}
	public int getValue() {
		return Value;
	}
	public void setValue(int value) {
		Value = value;
	}
}